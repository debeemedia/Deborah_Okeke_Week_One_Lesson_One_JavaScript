// Create and manipulate strings

// create variable
let name = 'Deborah'
console.log(name)

// concatenate string
name = 'Deborah ' + 'Okeke'
console.log(name)

// toUpperCase()
name = name.toUpperCase()
console.log(name)

// toLowerCase()
name = name.toLowerCase()
console.log(name)

// slice
name = name.slice(0, 7)
console.log(name)
